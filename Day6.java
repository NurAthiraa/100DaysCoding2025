public class Day6 {
        public static void main(String[] args) {
            // Tipe data float (single-precision)
            float xFloat = 12.345f;

            // Tipe data double (double-precision)
            double xDouble = 12.3456789012345;

            // Menampilkan hasil
            System.out.println("Nilai float: " + xFloat);
            System.out.println("Nilai double: " + xDouble);
        }
    }
